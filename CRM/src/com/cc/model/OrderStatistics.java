package com.cc.model;

public class OrderStatistics {

	private String sectionName;//部门名称
		
	private String userName;//用户姓名
	
	private int personOrderNum;//个人订单量
	
	private double personOrderAmount;//个人订单总金额

	private double sectionOrderNum;//部门订单量
	
	private double sectionOrderAmount;//部门订单总金额
	
	public String getSectionName() {
		return sectionName;
	}

	public void setSectionName(String sectionName) {
		this.sectionName = sectionName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public int getPersonOrderNum() {
		return personOrderNum;
	}

	public void setPersonOrderNum(int personOrderNum) {
		this.personOrderNum = personOrderNum;
	}

	public double getPersonOrderAmount() {
		return personOrderAmount;
	}

	public void setPersonOrderAmount(double personOrderAmount) {
		this.personOrderAmount = personOrderAmount;
	}

	public double getSectionOrderNum() {
		return sectionOrderNum;
	}

	public void setSectionOrderNum(double sectionOrderNum) {
		this.sectionOrderNum = sectionOrderNum;
	}

	public double getSectionOrderAmount() {
		return sectionOrderAmount;
	}

	public void setSectionOrderAmount(double sectionOrderAmount) {
		this.sectionOrderAmount = sectionOrderAmount;
	}

	@Override
	public String toString() {
		return "OrderStatistics [sectionName=" + sectionName + ", userName=" + userName + ", personOrderNum="
				+ personOrderNum + ", personOrderAmount=" + personOrderAmount + ", sectionOrderNum=" + sectionOrderNum
				+ ", sectionOrderAmount=" + sectionOrderAmount + "]";
	}

	
	
	
}
